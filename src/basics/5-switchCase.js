//switch case 
let category = "phone";
switch(category){
    case "phone": console.log("phone");break;
    case "computer": console.log("computer");break;
    default: console.log("phone");break;
};


let x =10;
switch (x) {
    case x>10:
        console.log("x > 10");
        break;
    case x<10:
        console.log("x < 10");
        break;
    
    default: console.log("x == 10");
        break;
}